package corejava.core_java;

public class Datatypes {

    public static void main(String[] args) {
        char grade = '3'; // Stores the character 'A'
        char symbol = '$'; // Stores the dollar symbol '$'
        char newline = '\n'; // Stores a newline character
        System.out.println(grade);


        byte age = 30; // Stores the value 30
        byte temperature = -128; // Stores a negative value
        byte bitmask = 0b101010; // Stores a binary value (42 in decimal)

    }
}
